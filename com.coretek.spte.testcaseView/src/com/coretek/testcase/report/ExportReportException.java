public class ExportReportException extends Exception
{

	private static final long	serialVersionUID	= 6945168978187782840L;

	public ExportReportException()
	{
		super();

	}

	public ExportReportException(String message, Throwable cause)
	{
		super(message, cause);

	}

	public ExportReportException(String message)
	{
		super(message);

	}

	public ExportReportException(Throwable cause)
	{
		super(cause);

	}

}
